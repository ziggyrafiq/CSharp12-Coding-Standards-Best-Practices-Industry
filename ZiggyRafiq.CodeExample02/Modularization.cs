// Example of modularization
namespace ZiggyRafiq.CodeExample02;

// Define a class to encapsulate related functionality
public class ExampleClass
{
    // Class implementation

    // Example method within the class
    public void EampleMethod()
    {
        // Method implementation
        Console.WriteLine("EampleMethod() is executed.");
    }
}

// We can define more classes or modules within the same namespace
public class AnotherExampleClass
{
    // Class implementation

    // Example method within the class
    public void AnotherExampleMethod()
    {
        // Method implementation
        Console.WriteLine("AnotherExampleMethod() is executed.");
    }
}